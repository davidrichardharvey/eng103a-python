# The following my_functions
# Each should take 2 arguments
# And return the appropriate calculation

# add
# subtract
# multiply
# divide

def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2